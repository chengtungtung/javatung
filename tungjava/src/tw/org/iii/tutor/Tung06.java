package tw.org.iii.tutor;

public class Tung06 {

	public static void main(String[] args) {
		// 布林值(在java中，0就是0，不代表false,1就是1，不代表true)
		
		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1);
		System.out.println(b2);
		
		/*
		印出：
		true
		false
		 */
		

	}

}
