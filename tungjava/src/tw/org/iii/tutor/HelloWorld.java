package tw.org.iii.tutor; // 第一句
// 底下HelloWorld 要跟檔名一致
public class HelloWorld {
	// 底下為程式進入點，java大小寫要明確
	// static指靜態，與物件無關，err和out為輸出，in為輸入
	public static void main(String[] args){
		// system是指作業系統，out是指輸出，;要做結尾
		// 錯誤有分：1.編譯時錯誤 2.執行時錯誤
		// 快捷鍵 sysout alt+/
		// \n 為跳脫字元，可以換列
		// println 可以換列
		System.out.println("Hello, World, tung");
		System.out.print("Hello, World\n");
		System.out.print("Hello, World");
		
		
	}
	
}
